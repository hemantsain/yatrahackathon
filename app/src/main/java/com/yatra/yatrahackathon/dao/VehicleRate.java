public class VehicleRate {

    private String RatePeriod;
    private String UnlimitedMileage;
    private String Units;
    private String RateSource;
    private String RateAvailability;
    private String RateCode;
    private String RateCategory;

    public String getRatePeriod() {
        return RatePeriod;
    }

    public void setRatePeriod(String ratePeriod) {
        RatePeriod = ratePeriod;
    }

    public String getUnlimitedMileage() {
        return UnlimitedMileage;
    }

    public void setUnlimitedMileage(String unlimitedMileage) {
        UnlimitedMileage = unlimitedMileage;
    }

    public String getUnits() {
        return Units;
    }

    public void setUnits(String units) {
        Units = units;
    }

    public String getRateSource() {
        return RateSource;
    }

    public void setRateSource(String rateSource) {
        RateSource = rateSource;
    }

    public String getRateAvailability() {
        return RateAvailability;
    }

    public void setRateAvailability(String rateAvailability) {
        RateAvailability = rateAvailability;
    }

    public String getRateCode() {
        return RateCode;
    }

    public void setRateCode(String rateCode) {
        RateCode = rateCode;
    }

    public String getRateCategory() {
        return RateCategory;
    }

    public void setRateCategory(String rateCategory) {
        RateCategory = rateCategory;
    }
}
